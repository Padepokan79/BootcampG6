package hashMapPertandinganBola;

public class Tiket {
	private String jenis;
	private double harga;
	
	public Tiket(String jenis, double harga) {
		super();
		this.jenis = jenis;
		this.harga = harga;
	}

	public String getJenis() {
		return jenis;
	}

	public void setJenis(String jenis) {
		this.jenis = jenis;
	}

	public double getHarga() {
		return harga;
	}

	public void setHarga(double harga2) {
		this.harga = harga2;
	}
	
	
}
