
public class Pedagang {
	private String nama, alamat, barang;

	public Pedagang(String nama, String alamat, String barang) {
		super();
		this.nama = nama;
		this.alamat = alamat;
		this.barang = barang;
	}

	public String getNama() {
		return nama;
	}

	public String getAlamat() {
		return alamat;
	}

	public String getBarang() {
		return barang;
	}
	
}
