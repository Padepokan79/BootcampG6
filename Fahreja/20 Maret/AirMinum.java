
public class AirMinum {
	private String nama;
	private int harga, galon;
	public AirMinum(String nama, int harga, int galon) {
		super();
		this.nama = nama;
		this.harga = harga;
		this.galon = galon;
	}
	public String getNama() {
		return nama;
	}
	public int getHarga() {
		return harga;
	}
	public int getGalon() {
		return galon;
	}
	
}
