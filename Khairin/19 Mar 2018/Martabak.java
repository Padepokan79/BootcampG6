public class Martabak {
	String namaMartabak;
	private double hargaMartabak;
	public Martabak(String namaMartabak, double hargaMartabak) {
		super();
		this.namaMartabak = namaMartabak;
		this.hargaMartabak = hargaMartabak;
	}
	public String getNamaMartabak() {
		return namaMartabak;
	}
	public void setNamaMartabak(String namaMartabak) {
		this.namaMartabak = namaMartabak;
	}
	public double getHargaMartabak() {
		return hargaMartabak;
	}
	public void setHargaMartabak(double hargaMartabak) {
		this.hargaMartabak = hargaMartabak;
	}
	

}
