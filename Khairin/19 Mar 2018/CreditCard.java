public class CreditCard {
	String tipeKartu;
	int iuranTahunan;
	
	
	public CreditCard(String tipeKartu, int iuranTahunan) {
		super();
		this.tipeKartu = tipeKartu;
		this.iuranTahunan = iuranTahunan;
	}
	public String getTipeKartu() {
		return tipeKartu;
	}
	public void setTipeKartu(String tipeKartu) {
		this.tipeKartu = tipeKartu;
	}
	public int getIuranTahunan() {
		return iuranTahunan;
	}
	public void setIuranTahunan(int iuranTahunan) {
		this.iuranTahunan = iuranTahunan;
	}	
		
}
	
