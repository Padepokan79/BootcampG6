package DuaPuluhMaret2018;

public class Printing {
	String jenisPrinting;
	private int hargaPrinting;
	
	public Printing(String jenisPrinting, int hargaPrinting) {
		super();
		this.jenisPrinting = jenisPrinting;
		this.hargaPrinting = hargaPrinting;
	}

	public int getHargaPrinting() {
		return hargaPrinting;
	}

	public void setHargaPrinting(int hargaPrinting) {
		this.hargaPrinting = hargaPrinting;
	}

}
